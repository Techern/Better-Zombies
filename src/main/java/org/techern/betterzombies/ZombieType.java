package org.techern.betterzombies;

/**
 * A quick overview of each type of {@link net.minecraft.world.entity.monster.Zombie}
 *
 * @since 0.1
 */
public enum ZombieType {

    /**
     * A regular old zombie
     *
     * @since 0.1
     */
    ZOMBIE,

    /**
     * A baby zombie
     *
     * @since 0.1
     */
    ZOMBIE_BABY,

    /**
     * A zombie villager
     *
     * @since 0.1
     */
    ZOMBIE_VILLAGER,

    /**
     * A zombie that spawned in the desert
     *
     * @since 0.1
     */
    ZOMBIE_HUSK,

    /**
     * A zombie that spawned underwater
     *
     * @since 0.1
     */
    ZOMBIE_DROWNED,

    /**
     * Not a horse, of course, of course!
     *
     * @since 0.1
     */
    ZOMBIE_HORSE,

    /**
     * Spider-pig, spider-pig!
     * Does whatever a spider-pig does!
     * Can he swing from a web?
     * No, he can't, he's a pig!
     * Look out, he is a spider-pig!
     *
     * @since 0.1
     */
    ZOMBIE_HOGLIN;

}
